    private void tabelaAnalistaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaAnalistaMouseClicked
        int linha = tabelaAnalista.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tabelaAnalista.getModel();
        campoID.setText(Long.toString((long) model.getValueAt(linha, 0)));
        campoNome.setText((String) model.getValueAt(linha, 1));
        if (model.getValueAt(linha, 2).equals(true)) {
            radioSim.setSelected(true);
        } else {
            radioNao.setSelected(true);
        }

        botaoExcluir.setEnabled(true);
    }//GEN-LAST:event_tabelaAnalistaMouseClicked

