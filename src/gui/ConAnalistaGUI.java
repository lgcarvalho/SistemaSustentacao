    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        Long id = Long.parseLong(campoID.getText());
        String nome = campoNome.getText();
        AnalistaDAO dao = new AnalistaDAO();

        try {
            dao.remover(Analista.class, id);
            JOptionPane.showMessageDialog(this, "Analista " + nome + " excluÃ­do com sucesso!!");

            carregaTabela();
            limpaCampos();

            botaoExcluir.setEnabled(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Long id = Long.parseLong(campoID.getText());
        String nome = campoNome.getText();
        boolean desligado;
        if (grupoDesligado.isSelected(radioSim.getModel())) {
            desligado = true;
        } else {
            desligado = false;
        }

        Analista analista = new Analista(nome, desligado);
        analista.setId(id);

        AnalistaDAO dao = new AnalistaDAO();

        try {
            dao.salvar(analista);

            JOptionPane.showMessageDialog(this, "Analista " + nome + " alterado com sucesso!!");

            carregaTabela();
            limpaCampos();

            botaoExcluir.setEnabled(false);
            botaoAlterar.setEnabled(true);
            botaoSalvar.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void carregaTabela() {
        AnalistaDAO dao = new AnalistaDAO();
        List<Analista> lista = dao.consultaTodosAnalistas();

        DefaultTableModel model = (DefaultTableModel) tabelaAnalista.getModel();
        model.setRowCount(0);
        for(Analista analista : lista) {
            model.addRow(new Object[]{analista.getId(), analista.getNome(), analista.isDesligado()});
        }

        tabelaAnalista.setModel(model);
    }

    private void configuraTabela() {
        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();

        rightRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        tabelaAnalista.getColumnModel().getColumn(0).setMaxWidth(30);
        tabelaAnalista.getColumnModel().getColumn(2).setMaxWidth(100);
        tabelaAnalista.getColumnModel().getColumn(0).setCellRenderer(rightRenderer);
        tabelaAnalista.getColumnModel().getColumn(1).setCellRenderer(rightRenderer);
        tabelaAnalista.getColumnModel().getColumn(2).setCellRenderer(rightRenderer);
        tabelaAnalista.getTableHeader().setReorderingAllowed(false);
        tabelaAnalista.getTableHeader().setResizingAllowed(false);
    }

    private void limpaCampos() {
        campoID.setText(null);
        campoNome.setText(null);
        grupoDesligado.clearSelection();
    }

